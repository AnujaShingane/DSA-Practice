class Solution {
    public int numIslands(char[][] grid) {
        int n = grid.length;
        int m = grid[0].length;
        int cnt = 0;

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                if (grid[i][j] == '1') {
                    cnt++;
                    dfs(i, j, grid);
                }
            }
        }
        return cnt;
    }

    public void dfs(int i, int j, char[][] grid) {
        int n = grid.length;
        int m = grid[0].length;

        if (i < 0 || j < 0 || i >= n || j >= m || grid[i][j] == '0') {
            return;
        }

        grid[i][j] = '0'; // mark visited

        dfs(i - 1, j, grid); // up
        dfs(i + 1, j, grid); // down
        dfs(i, j - 1, grid); // left
        dfs(i, j + 1, grid); // right
    }
}
